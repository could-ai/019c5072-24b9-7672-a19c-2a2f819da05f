class Student {
  final String id;
  final String name;
  final DateTime dateOfBirth;
  final String gender;
  final String schoolName;
  final String centerNumber;

  Student({
    required this.id,
    required this.name,
    required this.dateOfBirth,
    required this.gender,
    required this.schoolName,
    required this.centerNumber,
  });
}
